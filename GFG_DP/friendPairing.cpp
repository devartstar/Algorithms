int solve() {
	int n;
	cin>>n;
	int dp[n+1];
	memset(dp,0,sizeof(dp));
	for(int i=0;i<=n;i++){
		if(i<=2){
			dp[i]=i;
		}else
			dp[i]=dp[i-1]+(i-1)*dp[i-2];
	}
	return dp[n];
}

