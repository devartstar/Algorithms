const int x = 10000;
vector<bool> isPrime(x+1, true); 

void compute() {
    isPrime[0] = false;
    isPrime[1] = false;

    for(int i = 2; i<=x; i++){
        if(isPrime[i]){
            for(int j=i*2; j<=x; j+=i){
                isPrime[j] = false;
            }
        }
    }
}


bool isSubSequence(string str1, string str2)
{
    int m = (int)str1.length();
    int n = (int)str2.length();
    int j = 0; 
 
    for (int i = 0; i < n && j < m; i++)
        if (str1[j] == str2[i])
            j++;
 
    return (j == m);
}

