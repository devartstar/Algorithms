void solve() {
    ll p,f; cin>>p>>f;
    ll cnts, cntw; cin>>cnts>>cntw;
    ll s, w; cin>>s>>w;
    ll ans = 0;
    ll as,aw,bs,bw;
    for(ll as = 0; as <= cnts; as++){
        ll sleft = cnts, wleft = cntw;
        if(as*s > p){
            break;
        }
        sleft = sleft - as;
        aw = min((p - as*s) / w, wleft);
        wleft = wleft - aw;
        if(s < w){
            bs = min(f / s, sleft);
            bw = min((f - bs*s)/w, wleft);
        }else{
            bw = min(f/w, wleft);
            bs = min((f-bw*w)/s, sleft);
        }
        ans = max(ans, as + aw + bs + bw);
    }

    for(ll bs = 0; bs <= cnts; bs++){
        ll sleft = cnts, wleft = cntw;
        if(bs*s > f){
            break;
        }
        sleft = sleft - bs;
        bw = min((f - bs*s) / w, wleft);
        wleft = wleft - bw;
        if(s < w){
            as = min(p / s, sleft);
            aw = min((p - as*s)/w, wleft);
        }else{
            aw = min(p/w, wleft);
            as = min((p-aw*w)/s, sleft);
        }
        ans = max(ans, as + aw + bs + bw);
    }

    cout<<ans<<endl;
}

